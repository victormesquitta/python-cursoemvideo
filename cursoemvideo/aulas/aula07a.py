n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro: '))
so = n1 + n2
sub = n1 - n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
print('A soma é {}, a subtração é {} e a multiplicação é {}.'.format(so, sub, m))
print('A divisão é {:.3f}, a divisão inteira é {} e a exponenciação é {}.'.format(d, di, e))
